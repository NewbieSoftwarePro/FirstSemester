#include<iostream>
using namespace std;
int main()
{
	int num;
	cout<<"input num between 1 and 10:";
	cin>>num;
	switch(num){
	case 1:
		cout<<"I";
		break ;
	case 2:
		cout<<"II";
		break;
	case 3:
		cout<<"III";
		break;
	case 4:
		cout<<"IV";
		break;
	case 5:
		cout<<"V";
		break;
	case 6:
		cout<<"VI";
		break;
	case 7:
		cout<<"VII";
		break;
	case 8:
		cout<<"VIII";
		break;
	case 9:
		cout<<"XI";
		break;
	case 10:
		cout<<"X";
		break;
	default:
		cout<<"Input Validation: Please enter number between 1 and 10";
}
	return 0;	
		
	
		
}
